public class StringExercise {
    public static void main(String[] args) {
        String newString = "We don't need no education \nWe don't need no thought control.";
        System.out.println(newString);
        newString = "Check \"this out!\", \"s inside of \"s!";
        System.out.println(newString);
        newString = "In windows, the main drive is usually C:\\";
        System.out.println(newString);
        newString = "I can do backslashes \\, double backslashes \\\\, \nand the amazing triple backslash \\\\\\!";
        System.out.println(newString);
    }
}
